using System.Collections;

namespace MO.Core.Forms.Common
{
   //============================================================
   // <T>基础集合接口。</T>
   //============================================================
   public interface IBaseCollection<T> : IList, ICollection, IEnumerable
   {
      //============================================================
      // <T>判断是否为空。</T>
      //
      // @return 是否为空
      //============================================================
      bool IsEmpty { get; }
      
      //============================================================
      // <T>获得对象内存。</T>
      //
      // @return 对象内存
      //============================================================
      T[] Items { get; }

      //============================================================
      // <T>追加对象到当前集合中。</T>
      //
      // @param value 对象
      // @return 索引位置
      //============================================================
      int Add(T item);

      //============================================================
      // <T>追加对象集合到当前集合中。</T>
      //
      // @param values 对象集合
      //============================================================
      void AddRange(params object[] values);

      //============================================================
      // <T>追加对象集合到当前集合中。</T>
      //
      // @param values 对象集合
      //============================================================
      void AddRange(params T[] values);
      
      //============================================================
      // <T>追加对象到尾位置。</T>
      //
      // @param value 对象
      //============================================================
      void Push(T value);
   
      //============================================================
      // <T>移除指定的对象。</T>
      //
      // @param value 对象
      //============================================================
      void Remove(T item);
   }
}
