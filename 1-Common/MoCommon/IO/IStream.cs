namespace MO.Common.IO
{
   //============================================================
   // <T>数据流</T>
   //============================================================
   public interface IStream : IInput, IOutput
   {
      //============================================================
      // <T>获取和设置数据流位置。</T>
      //============================================================
      int Position { get; set; }

      //============================================================
      // <T>重设数据流开始位置和长度。</T>
      //============================================================
      void Flip();

      //============================================================
      // <T>重置数据流。</T>
      //============================================================
      void Reset();

      //============================================================
      // <T>清除所有数据内容。</T>
      // <P>不清空数据，只是重设长度和位置。</P>
      //============================================================
      void Clear();
   }
}
