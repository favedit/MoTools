namespace MO.Common.Geom
{
   //============================================================
   // <T>三维点。</T>
   //============================================================
   public class SPoint3<T>
   {
      public T X;

      public T Y;

      public T Z;

      //============================================================
      // <T>构造三维点。</T>
      //============================================================
      public SPoint3() {
      }

      //============================================================
      // <T>构造三维点。</T>
      //
      // @param x X坐标
      // @param y Y坐标
      // @param z Z坐标
      //============================================================
      public SPoint3(T x, T y, T z) {
         X = x;
         Y = y;
         Z = z;
      }

      //============================================================
      // <T>接收二维点对象。</T>
      //
      // @param point 二维点对象
      //============================================================
      public void Assign(SPoint3<T> point) {
         X = point.X;
         Y = point.Y;
         Z = point.Z;
      }

      //============================================================
      // <T>设置二维点。</T>
      //
      // @param x X坐标
      // @param y Y坐标
      // @param z Z坐标
      //============================================================
      public void Set(T x, T y, T z) {
         X = x;
         Y = y;
         Z = z;
      }

      //============================================================
      // <T>格式化成字符串。</T>
      //
      // @return 字符串
      //============================================================
      public override string ToString() {
         return X + "," + Y + "," + Z;
      }
   }
}
